//Q4. Print multiples of 3 within the number range 1 to 30

public class MultipleOf3 {
    public static void main(String[] args) {
        System.out.println("Multiple Of 3");
        for(int i = 1; i <=30; i++) {
            if(i%3 == 0) {
                System.out.print(i+" ");
            }

        }
    }
    
}
